package bcu.lyg.pojo;

import java.util.List;

public class PersonalAlbum {
	private String name;
	private List pAlbums;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public List getpAlbums() {
		return pAlbums;
	}
	public void setpAlbums(List pAlbums) {
		this.pAlbums = pAlbums;
	}
}
